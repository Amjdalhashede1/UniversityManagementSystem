    public void fillToComboboxYearDate(JComboBox comboBox) {
        String yearModel[] = new String[20];
        int date = Integer.parseInt(db.go.getYear() + "");
        int k = 2010;
        int j = 0;
        for (int i = k; i <= date && j <= yearModel.length; i++) {
            yearModel[j] = i + " \\ " + (i + 1) + "";
            j++;
        }
        comboBox.setModel(new DefaultComboBoxModel(yearModel));
        comboBox.setSelectedItem(yearModel[(j - 1)]);
    }

