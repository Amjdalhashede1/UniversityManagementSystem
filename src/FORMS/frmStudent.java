    private Map<String, Object> setValues() {
        dept.setTable(TABLE.DEPARTMENTS);
        dept.setBy(DEPT_NAME);
        dept.setGet(DEPT_ID);
        Map<String, Object> values = new HashMap<>();
        values.put(REGESTY_NUMBER + "", lblRegestry_number.getText());
        values.put(STD_ID + "", lblStd_id.getText());
        values.put(STD_NAME + "", txtStd_name.getText());
        values.put(DEPT_ID + "", dept.getValueBy(cbxDept_name.getSelectedItem()));
        values.put(STD_GENDER + "", (rdbMale.isSelected() ? rdbMale.getText() : rdbFamel.getText()));
        values.put(DATE_JOIN + "", db.go.getDate(date_join));
        values.put(SCHOOLYEAR + "", cbxYearDate.getSelectedItem());
        values.put(BATCHNUMBER + "", lblBatchNumber.getText());
        return values;
    }

    private Map<String, Object> setImg() {
        Map<String, Object> photoes = new HashMap<>();
        if (personalImg != null) {
            photoes.put(REGESTY_NUMBER + "", lblRegestry_number.getText());
            photoes.put(PHOTO_ID + "", db.go.getAutoNumber(TABLE.PHOTOES + "", PHOTO_ID + ""));
            photoes.put(RESOURSE + "", TABLE.RESOURSE.STUDENT + "");
            photoes.put(PHOTO_TYPE + "", TABLE.DOCEMENT_TYPE.PERSONAL + "");
            photoes.put(PHOTO + "", personalImg);
        }
        return photoes;
    }

    private ArrayList<Map.Entry<Integer,Map<String, Object>>> setPhones() {
        ArrayList<Map.Entry<Integer,Map<String, Object>>> arr=new ArrayList<>();
        Map<String, Object> phones = new HashMap<>();
        int index=0;
        if (tblPhones.getRowCount() > 0) {
            for (int i = 0; i < tblPhones.getRowCount(); i++) {
                phones.put(REGESTY_NUMBER + "", lblRegestry_number.getText());
                phones.put(PHONE_TYPE + "", tblPhones.getValueAt(i,1));
                phones.put(PHONE_NUMBER + "", tblPhones.getValueAt(i, 0));
                arr.add(new AbstractMap.SimpleEntry(index++,phones));
                phones=new HashMap<>();
            }
        }
            return arr;
    }

    private void clearData() {
        Tools.ClearText(this);
        btnAdd.setEnabled(true);
        btnDelete.setEnabled(false);
        btnEdit.setEnabled(false);
        lblStd_id.setText(std.getAutoNumber(STD_ID) + "");
        lblBatchNumber.setText(std.getAutoNumber(BATCHNUMBER) + "");
        int reg = 798478984;
        if (Integer.parseInt(std.getAutoNumber(REGESTY_NUMBER)+"") > reg) {
            lblRegestry_number.setText( std.getAutoNumber(REGESTY_NUMBER)+ "");
        }else{
            lblRegestry_number.setText( reg+ "");
        }
        rdbMale.setSelected(true);
        txtStd_name.requestFocus();
        std.getAllRows(tblStudents);
        Tools.removeRowsInTable(tblPhones);
    }

    public void mouseClick() {
        try {
            int raw = tblStudents.getSelectedRow();
            Object Reg_num = tblStudents.getValueAt(raw, 0).toString();
            Map<String, Object> wherec = new HashMap<>();
            wherec.put(REGESTY_NUMBER + "", Reg_num);
            ResultSet res = db.go.getData(STUDENT_DATA + "", new ArrayList<>(), wherec);
            res.next();
            lblBatchNumber.setText(Reg_num + "");
            lblStd_id.setText(getValue(res, STD_ID + "") + "");
            lblBatchNumber.setText(getValue(res, BATCHNUMBER + "") + "");
            txtStd_name.setText(getValue(res, STD_NAME + "") + "");
            cbxDept_name.setSelectedItem(getValue(res, DEPT_NAME + ""));
            cbxYearDate.setSelectedItem(getValue(res, SCHOOLYEAR + ""));
            date_join.setDate((Date) getValue(res, DATE_JOIN + ""));
            Object type = getValue(res, STD_GENDER + "");
            if (type.toString().equals(MALE.getValue() + "")) {
                rdbMale.setSelected(true);
            } else {
                rdbFamel.setSelected(true);
            }
            btnEdit.setEnabled(true);
            btnDelete.setEnabled(true);
            btnAdd.setEnabled(false);
        } catch (SQLException ex) {
            Tools.MsgBox(ex.getMessage());
        }
    }
